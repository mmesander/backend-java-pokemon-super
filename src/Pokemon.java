package src;

public class Pokemon {
    private String name;
    private int weight;
    private int hp;
    private String attack;
    private String food;

    public Pokemon(String name, int weight, int hp, String attack, String food){
        this.name = name;
        this.weight = weight;
        this.hp = hp;
        this.attack = attack;
        this.food = food;
    }



}
